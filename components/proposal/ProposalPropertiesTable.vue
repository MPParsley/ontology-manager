<template>
  <table class="table generic-table is-fullwidth">
    <thead>
      <tr>
        <th>
          Property
        </th>
        <th>
          Type
        </th>
        <th>
          Used On
          <br>
          <small>these classes</small>
        </th>
        <th v-show="!disabled" />
      </tr>
    </thead>
    <tbody>
      <tr
        v-for="(property, index) in properties"
        :key="index">
        <td>
          <a
            v-if="property.isNew"
            :href="`#${property.label}`">
            {{ property.label }}
          </a>
          <a
            v-else
            :href="rebaseIRI(property.iri)">
            {{ property.label }}
          </a>
        </td>
        <td>
          <ul class="types-list">
            <li
              v-for="range in rangeOf(property.iri, dataset)"
              :key="range.value">
              <a
                :href="rebaseIRI(range.value)"
                rel="noopener noreferrer"
                target="_blank">
                {{ term(range.value) }}
              </a>
            </li>
          </ul>
        </td>
        <td>
          <ul>
            <li
              v-for="otherClass in usedOnClasses(property.iri, dataset)"
              :key="otherClass.object.value">
              <a
                v-if="classIsNew(otherClass)"
                :href="`#${ term(otherClass.object) }`">
                {{ term(otherClass.object) }}
              </a>
              <a
                v-else
                :href="rebaseIRI(otherClass.object.value)"
                rel="noopener noreferrer"
                target="_blank">
                {{ term(otherClass.object) }}
              </a>
            </li>
          </ul>
        </td>
        <td v-show="!disabled">
          <div class="is-pulled-right">
            <button
              class="table-action-button"
              @click.prevent="$emit('delete', index)">
              Remove
            </button>
            <a
              class="table-action-button"
              v-show="property.isNew"
              @click.prevent="jumpToProperty(property, index)">
              Edit
            </a>
          </div>
        </td>
      </tr>
    </tbody>
  </table>
</template>

<script>
import { term, termIRI, rebaseIRI, usedOnClasses, rangeOf } from '@/libs/rdf'

export default {
  name: 'ProposalPropertiesTable',
  props: {
    disabled: {
      type: Boolean,
      required: false,
      default: false
    },
    properties: {
      type: Array,
      required: true
    },
    dataset: {
      type: Object,
      required: true
    },
    storePath: {
      type: String,
      required: true
    }
  },
  methods: {
    term,
    rebaseIRI,
    usedOnClasses,
    rangeOf,
    classIsNew ({ object }) {
      const ontology = this.$store.getters['graph/ontology']
      const found = ontology.match(object, termIRI.a, termIRI.class).toArray().length
      return !found
    },
    jumpToProperty (property, index) {
      if (this.storePath) {
        this.$vuexSet(`${this.storePath}.domains[${index}].collapsed`, false)
      }
      setTimeout(() => {
        try {
          document.getElementById(property.label).scrollIntoView()
        }
        catch (err) {}
      }, 25)
    }
  }
}
</script>
